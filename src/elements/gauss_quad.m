function [posgp, W] = gauss_quad(eltype, ngpel)
%
%%%%%%%%%%% POSITION OF GAUSS POINT AND ITS WEIGHT %%%%%%%%%%%%%%
%
%  INPUT
%    eltype : Type of element
%    ngpel  : Number of gauss point of i-element
%
%  OUTPUT
%    posgp  : Position of gauss point
%    W      : Weight
%
%  REFERENCE
%    J Fish & T Belytschko. A first course in finite element
%    analysis. Wiley, Chichester, 2007.
%
% ...

%------- Rectangular Element -----------------------------------%
  if (eltype == 5 || eltype == 6)                               %
%---------------------------------------------------------------%
    switch (ngpel)
      case 1
        posgp(1,1) = 0.0D0;
        posgp(2,1) = 0.0D0;
        W(1) = 4.0D0;
      case 4
        posgp(1,1) = -0.577350269189626D0;
        posgp(2,1) = -0.577350269189626D0;
        W(1) = 1.0D0;
        posgp(1,2) = -0.577350269189626D0;
        posgp(2,2) =  0.577350269189626D0;
        W(2) = 1.0D0;
        posgp(1,3) =  0.577350269189626D0;
        posgp(2,3) = -0.577350269189626D0;
        W(3) = 1.0D0;
        posgp(1,4) =  0.577350269189626D0;
        posgp(2,4) =  0.577350269189626D0;
        W(4) = 1.0D0;
      case 5
        posgp(1,1)=-0.774596669241483D0;
        posgp(2,1)=-0.774596669241483D0;
        W(1)=0.555555555555556D0;
        posgp(1,2)=-0.774596669241483D0;
        posgp(2,2)=+0.774596669241483D0;
        W(2)=0.555555555555556D0;
        posgp(1,3)=+0.774596669241483D0;
        posgp(2,3)=-0.774596669241483D0;
        W(3)=0.555555555555556D0;
        posgp(1,4)=+0.774596669241483D0;
        posgp(2,4)=+0.774596669241483D0;
        W(4)=0.555555555555556D0;
        posgp(1,5)=+0.0D0;
        posgp(2,5)=+0.0D0;
        W(5)=1.777777777777778D0;
      case 9
        posgp(1,1)=-0.774596669241483D0;
        posgp(2,1)=-0.774596669241483D0;
        W(1)=0.308641975308643D0;
        posgp(1,2)=-0.774596669241483D0;
        posgp(2,2)=+0.0D0;
        W(2)=0.493827160493828D0;
        posgp(1,3)=-0.774596669241483D0;
        posgp(2,3)=+0.774596669241483D0;
        W(3)=0.308641975308643D0;
        posgp(1,4)=+0.0D0;
        posgp(2,4)=-0.774596669241483D0;
        W(4)=0.493827160493828D0;
        posgp(1,5)=+0.0D0;
        posgp(2,5)=+0.0D0;
        W(5)=0.790123456790124D0;
        posgp(1,6)=+0.0D0;
        posgp(2,6)=+0.774596669241483D0;
        W(6)=0.493827160493828D0;
        posgp(1,7)=+0.774596669241483D0;
        posgp(2,7)=-0.774596669241483D0;
        W(7)=0.308641975308643D0;
        posgp(1,8)=+0.774596669241483D0;
        posgp(2,8)=+0.0D0;
        W(8)=0.493827160493828D0;
        posgp(1,9)=+0.774596669241483D0;
        posgp(2,9)=+0.774596669241483D0;
        W(9)=0.308641975308643D0;
      otherwise
        error('gaus2d: invalid number of gauss point');
    end

%------- Triangular Element ------------------------------------%
  elseif (eltype == 3 || eltype == 4)                           %
%---------------------------------------------------------------%
    switch (ngpel)
      case 1
        posgp(1,1)=0.333333333333333D0;
        posgp(2,1)=0.333333333333333D0;
        W(1)=0.5D0;
      case 3
        posgp(1,1)=0.166666666666667D0;
        posgp(2,1)=0.166666666666667D0;
        W(1)=0.166666666666667D0;
        posgp(1,2)=0.666666666666667D0;
        posgp(2,2)=0.166666666666667D0;
        W(2)=0.166666666666667D0;
        posgp(1,3)=0.166666666666667D0;
        posgp(2,3)=0.666666666666667D0;
        W(3)=0.166666666666667D0;
      otherwise
        error('gaus2d: invalid number of gauss point');
    end

%------- In any other case -------------------------------------%
  else                                                          %
%---------------------------------------------------------------%
      error('gaus2d: invalid element number');
  end
end